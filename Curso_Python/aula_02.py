# Função Print 
# A função print() é uma das funcionalidades mais utilizadas em Python para imprimir objetos na saída padrão.
# Ela permite exibir informações na tela de forma simples e direta.
# A função print() aceita um ou mais argumentos e os exibe na ordem em que são fornecidos.

# O argumento "sep é igual SEPARADOR" serve para passar um separador entre os argumentos

# \r \n -> CRFL Serve para quebra de linha no windows 
#\n -> LF Serve para quebra de linha no Unix

print(12,34, sep="/", end="\n\r") 
print(56,78, sep='-', end="\n")
print(9,10, sep='-', end="\n")
